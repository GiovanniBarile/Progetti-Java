public class Calcola {
    
    public static float add(int a, int b){
    return a+b;
    }
    public static float sub(float a, float b){
    return a-b;
    }    
    public static float div(float a, float b){
    return a/b;
    }    
    public static float mult(float a, float b){
    return a*b;
    }
    
}
